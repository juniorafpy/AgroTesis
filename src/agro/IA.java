    private void cargaFila(){
        Object [] fila=new Object[4];
        fila[0]=txtid.getText();
        fila[1]=txtrpHembra.getText();
        fila[2]=txtHBPHembra.getText();
        fila[3]= txtcantidad.getText();
        m.addRow(fila);      
}
    
    
    
    private void guardaIA(){
         int cf = m.getRowCount();
          for(int i = 0; i<cf; i++){
        String sql = "INSERT INTO detalleia(id, idIA, Stockpajuelas, Cantidad) VALUES ("
                +nuid+  ","+ m.getValueAt(i, 0).toString()
                +","+m.getValueAt(i, 0).toString()+ ","+ m.getValueAt(i, 3).toString()+")";
        sql = "update stockpajuelas set Stock = Stock - "+m.getValueAt(i,3).toString()+
                   " where id ="+ m.getValueAt(i,3).toString();
          cargaFila();
          conn.actualizaTabla(sql);
          System.out.print(sql);
          }
    }

