        private void Nacimiento(){
         int cf = m.getRowCount();
          for(int i = 0; i<cf; i++){
        String sql = "INSERT INTO detallenacimiento(Nombre, RP, Pelaje, Peso) VALUES ("
               + m.getValueAt(i, 0).toString()+ ","+ m.getValueAt(i, 1).toString()
                + ","+ m.getValueAt(i, 2).toString() + ","+ m.getValueAt(i, 3).toString()+")";
       
        cargaFila();
          conn.actualizaTabla(sql);
          System.out.print(sql);
          }
    }
        private void cargaFila(){
        Object [] fila=new Object[4];
        fila[0]=txtid.getText();
        fila[1]=txtNombre.getText();
        fila[2]=txtRP.getText();
        fila[3]= txtPeso.getText();
        m.addRow(fila);      
}
