    private void InsertaDato() {
        if ("".equals(txtNombre.getText())) {
            JOptionPane.showMessageDialog(null, "Completar campo vacio");
        } else {
            String sql = "Insert into laboratoriovacunas(Nombre, RUC, Direccion, Telefono, Gmail) "
                    + "values('" + txtNombre.getText() + "', '" + txtRUC.getText() + "' , "
                    + "'" + txtDireccion.getText() + "','" + txtTelefono.getText() + "' , '" + txtGmail.getText() + "')";
            System.out.println(sql);
            cargaGrilla();

        }
    }

    private void ModificaDato() {
        String sql = "update laboratoriovacunas set Nombre = "
                + "'" + txtNombre.getText() + "',Direccion ='" + txtDireccion.getText()
                + "',Telefono ='" + txtTelefono.getText()
                + "',Gmail ='" + txtGmail.getText()
                + "' where RUC = '" + txtRUC.getText() + "'";
        System.out.println(sql);
        conn.actualizaTabla(sql);

    }

    private void BuscarRegistro() {

        String sql = "select * from laboratoriovacunas where RUC = '"
                + tabla.getValueAt(tabla.getSelectedRow(), 0).toString() + "'";
        System.out.println(sql);
        conn.traeDatos(sql);
        try {
            if (conn.resultado.next()) {
                txtRUC.setText(conn.resultado.getString("RUC"));
                txtNombre.setText(conn.resultado.getString("Nombre"));
                txtDireccion.setText(conn.resultado.getString("Direccion"));
                txtTelefono.setText(conn.resultado.getString("Telefono"));
                txtGmail.setText(conn.resultado.getString("Gmail"));

            }
        } catch (SQLException ex) {
            Logger.getLogger(LaboratorioVacunas.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

