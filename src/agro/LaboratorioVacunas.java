    private void cargaCombo(javax.swing.JComboBox cbo, String sql ){
     
       try {
           cbo.removeAllItems();
           conn.sentencia = conn.conexion.createStatement();
           conn.resultado = conn.sentencia.executeQuery(sql);
           while (conn.resultado.next())
               cbo.addItem(conn.resultado.getString("Descripcion"));  
           cbo.setSelectedItem(-1);
       } catch (SQLException ex) {
           Logger.getLogger(Personales.class.getName()).log(Level.SEVERE, null, ex);
       }
          
        }
    private void cargaGrilla() {
        m.setRowCount(0);
        String sql = "Select Nombre, RUC FROM laboratoriovacunas";
        String columna = "Nombre";
        try {
            if (!txtFiltro.getText().trim().isEmpty()) {
                if (cboBuscar.getSelectedIndex() == 1) {
                    columna = "RUC";
                }
                sql = sql + " where " + columna + " like '%" + txtFiltro.getText().trim() + "%' ";
            }
            System.out.println(sql);
            conn.sentencia = conn.conexion.createStatement();
            conn.resultado = conn.sentencia.executeQuery(sql);
            while (conn.resultado.next()) {
                m.addRow(new Object[]{conn.resultado.getString("Nombre"),
                    conn.resultado.getString("RUC")});
            }
        } catch (SQLException ex) {
            Logger.getLogger(LaboratorioVacunas.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

