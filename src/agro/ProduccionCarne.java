    private void idKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_idKeyPressed
         String sql = "SELECT id, Nombre, Stock  FROM Alimentacion WHERE id = '" +
        id.getText()+ "'";
        try {
            conn.traeDatos(sql);
            if(conn.resultado.next()){
                txtDescripcion.setText(conn.resultado.getString("Descripcion"));
                txtStock.setText(conn.resultado.getString("Stock"));
                id.setText(conn.resultado.getString("id"));
                btnguardar.requestFocus();
            }
            //
        } catch (SQLException ex) {
            Logger.getLogger(ProduccionCarne.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_idKeyPressed

